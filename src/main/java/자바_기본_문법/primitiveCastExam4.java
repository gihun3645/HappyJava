package 자바_기본_문법;

public class primitiveCastExam4 {
    public static void main(String[] args) {
        long x2 = 50;
        int i2 = (int)x2;
        System.out.println(x2);
        System.out.println(i2);

        long x3 = Long.MAX_VALUE;
        int i3 = (int)x3;
        System.out.println(x3);
        System.out.println(i3);
    }
}
